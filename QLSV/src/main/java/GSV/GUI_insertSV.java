    public final void showTB() {
        ArrayList<SinhVien> list = XLDL.getDanhSachSV();
        ArrayList<String> listkhoa = XLDL.getDanhSachKhoa();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] row = new Object[5];
        // Xóa dữ liệu hiện có trong JComboBox
        jComboBox1.removeAllItems();
        for (String khoa : listkhoa) {
            jComboBox1.addItem(khoa); // Đổ dữ liệu từ danh sách khoa vào JComboBox
        }
        for (SinhVien sv : list) {
            row[0] = sv.getMa();
            row[1] = sv.getTen();
            row[2] = sv.getGT();
            row[3] = sv.getNamSinh();
            row[4] = sv.getId();
            model.addRow(row);

        }
    }
