    public void excuteSQL(String sql, String message) {
        Connection connection = ketnoi.getConn();
        Statement st;
        try {
            st = connection.createStatement();
            if (st.executeUpdate(sql) == 1) {
                DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
                model.setRowCount(0);
                showTB();
                JOptionPane.showMessageDialog(null, "" + message + "");
            } else {
                JOptionPane.showMessageDialog(null, "not" + message + "");
            }
        } catch (HeadlessException | SQLException e) {
        }
    }
    //hiển thị bảng
    public final void showTB() {
        ArrayList<sanpham> list = ketnoi1.getlistLoaiSP();
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        Object[] row = new Object[4];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getMasp();
            row[1] = list.get(i).getSp();
            row[2] = list.get(i).getGia();
            row[3] = list.get(i).getMa();

            model.addRow(row);
        }
    }
